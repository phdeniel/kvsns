# Get local address
modprobe lnet
lctl network up &>> /dev/null
LOCAL_NID=`lctl list_nids | head -1`
export CLOVIS_LOCAL_ADDR=$LOCAL_NID:12345:33:100
export CLOVIS_HA_ADDR=$LOCAL_NID:12345:34:1
export CLOVIS_CONFD_ADDR=$LOCAL_NID:12345:33:100
export CLOVIS_PROF_OPT='<0x7000000000000001:0>'
export CLOVIS_PROFILE='<0x7000000000000001:0>'
export CLOVIS_PROC_FID='<0x7200000000000000:0>'
export CLOVIS_INDEX_DIR="/tmp"

echo "export CLOVIS_LOCAL_ADDR=$CLOVIS_LOCAL_ADDR"
echo "export CLOVIS_HA_ADDR=$CLOVIS_HA_ADDR"
echo "export CLOVIS_CONFD_ADDR=$CLOVIS_CONFD_ADDR"
echo "export CLOVIS_PROF_OPT='$CLOVIS_PROF_OPT'"
echo "export CLOVIS_PROFILE='$CLOVIS_PROFILE'"
echo "export CLOVIS_PROC_FID='$CLOVIS_PROC_FID'"
echo "export CLOVIS_INDEX_DIR=$CLOVIS_INDEX_DIR"
